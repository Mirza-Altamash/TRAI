export async function exportGeneric(opts: {
  kind: "Excel" | "PDF";
  filename: string;
  reportName: string;
  generatedBy: string;
  headers: string[];
  data: (string | number)[][];
  summary?: [string, string | number][];
  sheets?: { name: string; headers: string[]; data: (string | number)[][] }[];
}) {
  if (opts.kind === "Excel") {
    if (opts.sheets && opts.sheets.length) {
      const XLSX = await import("xlsx");
      const wb = XLSX.utils.book_new();
      if (opts.summary) {
        const s = XLSX.utils.aoa_to_sheet([
          ["Telecom Regulatory Authority of India"],
          [opts.reportName],
          [`Generated By: ${opts.generatedBy}`],
          [`Generated: ${formatIstDateTime(new Date().toISOString())}`],
          [],
          ["Metric", "Value"],
          ...opts.summary,
        ]);
        s["!cols"] = [{ wch: 28 }, { wch: 32 }];
        XLSX.utils.book_append_sheet(wb, s, "Summary");
      }
      for (const sh of opts.sheets) {
        const ws = XLSX.utils.aoa_to_sheet([sh.headers, ...sh.data]);
        ws["!cols"] = sh.headers.map(() => ({ wch: 20 }));
        XLSX.utils.book_append_sheet(wb, ws, sh.name.slice(0, 31));
      }
      const out = XLSX.write(wb, { type: "array", bookType: "xlsx" }) as ArrayBuffer;
      triggerBlobDownload(
        new Blob([out], {
          type: "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
        }),
        opts.filename,
      );
      return;
    }
    await writeExcel({
      filename: opts.filename,
      reportName: opts.reportName,
      generatedBy: opts.generatedBy,
      headers: opts.headers,
      data: opts.data,
      summary: opts.summary ?? [["Total Records", opts.data.length]],
    });
  } else {
    await writePdf({
      filename: opts.filename,
      reportName: opts.reportName,
      generatedBy: opts.generatedBy,
      headers: opts.headers,
      data: opts.data,
    });
  }
}

export async function exportAssigneeAssignments(
  kind: "Excel" | "PDF",
  report: any,
  generatedBy: string,
) {
  const stamp = formatIstDate(new Date()).replace(/-/g, "");
  const ASSIGNEE_HEADERS = [
    "Ticket ID",
    "Summary",
    "Assigned At",
    "Assigned By",
    "Assignee",
    "Status",
    "Assignee Actions",
  ];

  const data = report.tickets.map((t: any) => [
    t.ticketId,
    t.summary,
    formatIstDateTime(t.assignedAt),
    `${t.assignedBy.name} (${t.assignedBy.role})`,
    `${report.assignee.name} (${report.assignee.role}/${report.assignee.subrole || "-"})`,
    t.status,
    t.assigneeActions.map((a: any) => a.formatted).join(";\n"),
  ]);

  if (kind === "Excel") {
    await writeExcel({
      filename: `assignee-report-${report.assignee.userId}-${stamp}.xlsx`,
      reportName: `Assignee Assignment Report: ${report.assignee.name}`,
      generatedBy,
      headers: ASSIGNEE_HEADERS,
      data,
      summary: [
        ["Assignee Name", report.assignee.name],
        ["Assignee ID", report.assignee.userId],
        ["From Date", report.fromDate],
        ["To Date", report.toDate],
        ["Total Tickets", report.tickets.length],
      ],
    });
  } else {
    await writePdf({
      filename: `assignee-report-${report.assignee.userId}-${stamp}.pdf`,
      reportName: `Assignee Assignment Report: ${report.assignee.name} (${report.fromDate} to ${report.toDate})`,
      generatedBy,
      headers: ASSIGNEE_HEADERS,
      data,
      columnStyles: {
        1: { cellWidth: 150 }, // Summary
        6: { cellWidth: 250 }, // Assignee Actions
      },
    });
  }
}
